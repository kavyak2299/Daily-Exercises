public class DoWhile {

	public static void main(String[] args) {
		int a=0;
		do
		{
			System.out.println("Kavya");
			a++; //if not given it will print infinite times
		}
		while(a<=5); //total 6 times it will print because it will execute first then it will check for condition

	}

}
